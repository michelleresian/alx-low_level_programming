#include <stdio.h>
/**
 * add - adds two integers
 * @a: first int
 * @b: second int
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subs two integers
 * @a: first int
 * @b: second int
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divs two integers
 * @a: first int
 * @b: second int
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mods two integers
 * @a: first int
 * @b: second int
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
